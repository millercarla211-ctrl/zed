pub fn default_activation_config() -> ActivationConfig {
    ActivationConfig {
        wake_words: detect_wake_words(),
        push_to_talk: KeyboardShortcut {
            modifiers: vec![],
            key: "Return".to_string(),
        },
        hands_free_toggle: KeyboardShortcut {
            modifiers: vec!["ControlLeft".to_string(), "ShiftLeft".to_string()],
            key: "Space".to_string(),
        },
    }
}

pub fn detect_wake_words() -> Vec<WakeWordConfigItem> {
    let wake_dir = Path::new("models/wake_words");

    WAKE_COMMAND_DEFINITIONS
        .into_iter()
        .filter_map(|definition| {
            let model_path = wake_dir.join(definition.model_filename);
            model_path.exists().then(|| WakeWordConfigItem {
                command_key: definition.command_key.to_string(),
                phrase: definition.phrase.to_string(),
                model_path: model_path.to_string_lossy().into_owned(),
                threshold: definition.threshold,
                aliases: definition
                    .aliases
                    .iter()
                    .map(|alias| (*alias).to_string())
                    .collect(),
            })
        })
        .collect()
}

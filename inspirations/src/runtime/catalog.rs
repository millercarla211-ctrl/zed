fn manifest(
    key: &str,
    display_name: &str,
    family: &str,
    repo_id: &str,
    modality: Modality,
    local_path: Option<&str>,
    artifact_format: ArtifactFormat,
    preferred_runtime: RuntimeKind,
    fallback_runtimes: &[RuntimeKind],
    conversion_lanes: &[ConversionLane],
    minimum_memory_bytes: u64,
    quantization: Option<&str>,
    license: Option<&str>,
    redistributable: bool,
    gated: bool,
    local_only: bool,
    tags: &[&str],
) -> ModelManifest {
    ModelManifest {
        key: key.to_string(),
        display_name: display_name.to_string(),
        family: family.to_string(),
        repo_id: repo_id.to_string(),
        modality,
        local_path: local_path.map(str::to_string),
        artifact_format,
        preferred_runtime,
        fallback_runtimes: fallback_runtimes.to_vec(),
        conversion_lanes: conversion_lanes.to_vec(),
        minimum_memory_bytes,
        quantization: quantization.map(str::to_string),
        license: license.map(str::to_string),
        redistributable,
        gated,
        local_only,
        tags: tags.iter().map(|tag| (*tag).to_string()).collect(),
    }
}

